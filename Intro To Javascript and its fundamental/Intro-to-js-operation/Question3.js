// Differentiate between unary, binary, and ternary operators in JavaScript. Give examples of each.

// In JavaScript, operators are categorized based on the number of operands they work with:

// Unary Operators:

// Definition: Unary operators operate on a single operand.
// Example:

var x = 5;
var y = -x;  // Unary negation, changes the sign of x

// Binary Operators:

// Definition: Binary operators operate on two operands.
// Example:

var a = 10;
var b = 20;
var sum = a + b;  // Addition is a binary operation

// Ternary Operator (Conditional Operator):

// Definition: The ternary operator is a shorthand for an if-else statement and operates on three operands.

var age = 20;
var result = (age >= 18) ? "Adult" : "Minor";
